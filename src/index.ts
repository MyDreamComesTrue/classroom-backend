import express from "express";

const app = express();
const PORT = 8000;

app.use(express.json());
app.use(express.urlencoded({ extended: true }));

app.get("/", (req, res) => {
  res.send("Hello, welcome to the classroom backend!");
})

app.listen(PORT, () => {
  console.log(`Server is running on port ${PORT}`);
})